package com.ckey.shijing.util;

public class UploadJson {
	private String _location;
	private String _name;
	private String small_picture;
	private String des;
	private String origin_picture;
	public String get_location() {
		return _location;
	}
	public void set_location(String _location) {
		this._location = _location;
	}
	public String get_name() {
		return _name;
	}
	public void set_name(String _name) {
		this._name = _name;
	}
	public String getSmall_picture() {
		return small_picture;
	}
	public void setSmall_picture(String small_picture) {
		this.small_picture = small_picture;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public String getOrigin_picture() {
		return origin_picture;
	}
	public void setOrigin_picture(String origin_picture) {
		this.origin_picture = origin_picture;
	}
	
}
